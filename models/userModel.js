const { Schema, model } = require("mongoose");

const UserModel = new Schema({
  email: {
    type: String,
    unique: true,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  gender: {
    type: String,
    required: true,
  },
  birthday: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    default: "user",
  },
  imageUrl: {
    type: String,
    required: false,
  },
});

module.exports = model("user", UserModel);
